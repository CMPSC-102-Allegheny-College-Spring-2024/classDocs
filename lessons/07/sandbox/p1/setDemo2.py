print("Application 2: Unique Elements in a Collection")

# Simulate a data collection with duplicate entries
data_collection = [1, 2, 3, 4, 5, 2, 3, 6, 7, 8, 5, 9]

# Use a set to get unique elements
unique_elements = set(data_collection)

# Display results
print(f"Original Collection: {data_collection}")
print(f"Unique Elements: {unique_elements}")
